// 1. Planning (Lập kế hoạch):
// Xác định mục tiêu của hệ thống, phạm vi dự án, đối tượng sử dụng (giảng viên, sinh viên, quản lý khoa), thời gian thực hiện, nguồn lực và công nghệ sẽ sử dụng (ví dụ: web, mobile, cơ sở dữ liệu).

// 2. Analysis (Phân tích yêu cầu):
// Thu thập và phân tích yêu cầu từ người dùng: giảng viên cần điểm danh lớp, sinh viên cần xem lịch sử điểm danh, bộ phận quản lý cần xuất báo cáo. Xác định các chức năng chính, dữ liệu cần lưu trữ và luồng nghiệp vụ.

// 3. Design (Thiết kế hệ thống):
// Thiết kế giao diện người dùng, sơ đồ cơ sở dữ liệu (bảng sinh viên, lớp học, buổi học, điểm danh), và kiến trúc hệ thống. Lên bản vẽ UML như Use Case, Class Diagram, ERD,...

// 4. Implementation (Lập trình / Triển khai):
// Viết mã chương trình theo bản thiết kế: xây dựng giao diện ứng dụng, chức năng đăng nhập, quét mã QR điểm danh, lưu thông tin vào cơ sở dữ liệu, phân quyền người dùng.

// 5. Testing (Kiểm thử):
// Kiểm tra các chức năng: đăng nhập, điểm danh, xem báo cáo, kiểm thử lỗi, bảo mật và hiệu năng. Sửa lỗi (debug) trước khi đưa vào sử dụng chính thức.

// 6. Deployment & Maintenance (Triển khai & Bảo trì):
// Triển khai hệ thống cho giảng viên và sinh viên sử dụng, hướng dẫn cách dùng. Sau khi triển khai, theo dõi, cập nhật phiên bản, sửa lỗi, tối ưu hiệu năng và bổ sung tính năng mới khi có yêu cầu.
