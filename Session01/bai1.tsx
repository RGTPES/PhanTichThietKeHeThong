//1 Giao dịch bán hàng
// Mục tiêu: Ghi nhận và xử lý các hoạt động bán hàng hàng ngày (bán sản phẩm, xuất hóa đơn, cập nhật tồn kho, thanh toán,...)
// Đặc điểm: Xử lý dữ liệu giao dịch thường xuyên, khối lượng lớn, yêu cầu chính xác và nhanh chóng.
// → Loại hệ thống: TPS (Transaction Processing System) – Hệ thống xử lý giao dịch.

//2 Phân tích xu hướng kinh doanh
// Mục tiêu: Phân tích dữ liệu lịch sử để hỗ trợ ra quyết định (doanh thu, mặt hàng bán chạy, khu vực tiềm năng,...)
// Đặc điểm: Sử dụng dữ liệu tổng hợp từ TPS, phân tích – dự báo xu hướng, dành cho quản lý trung cấp.
// → Loại hệ thống: DSS (Decision Support System) – Hệ thống hỗ trợ ra quyết định.

//3 Bảng tổng quan hiệu suất hàng tháng dành cho CEO
// Mục tiêu: Cung cấp thông tin tổng hợp ở mức chiến lược (doanh thu, lợi nhuận, chi phí, KPI, biểu đồ xu hướng).
// Đặc điểm: Thông tin tổng hợp, trực quan, hỗ trợ lãnh đạo cấp cao ra quyết định nhanh.
// → Loại hệ thống: EIS (Executive Information System) – Hệ thống thông tin dành cho lãnh đạo.
